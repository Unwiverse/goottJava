package goott;

public class Basic_05 {

	public static void main(String[] args) {
		System.out.println(10+23); // 숫자 + 숫자 = 덧셈연산 
		
		System.out.println("10"+23); // 문자+숫자 = 문자
		System.out.println(10+"23"); // 1023
		System.out.println("10"+4+7); // 문자+숫자+숫자= 문자
		System.out.println("10"+(4+7));
		}

}
