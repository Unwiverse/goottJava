package goott;

/*
 * java에서의 제어 문자.
 * - \t: 일정한 간격으로 띄어쓰기를 해주는 제어문자.
 * - \n: 줄바꿈 제어문자. 
 * - \
 * 
 */

public class Basic_03 {

	public static void main(String[] args) {
		System.out.println("프로그램 시작");
		System.out.print("java\t");
		System.out.print("programming\n");
		System.out.println("프로그램 종료");
	}

}
