package goott;

/*
 * 논리 자료형(boolean형)
 * - 참이나 거짓을 판별하는 자료형.
 * - true: 참인 값.
 * - false: 거짓인 값.
 * - boolean형의 기본 값은 false임.
 * - True / False(X) ==> 소문자로 작성. 
 */

public class Variable_07 {

	public static void main(String[] args) {
		boolean test = false;
		boolean test1 = 5>10;
		boolean test2 = 5<10;
		
		System.out.println("test>>>"+test);
		System.out.println("test1>>>"+test1);
		System.out.println("test2>>>"+test2);

	}

}
